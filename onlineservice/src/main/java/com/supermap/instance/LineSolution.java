package com.supermap.instance;

public class LineSolution {
	private String walkDistance;
	private String trafficInfo;
	private String direction;
	private String startName;
	private String endName;
	private int passStopCount;
	public String getWalkDistance() {
		return walkDistance;
	}
	public void setWalkDistance(String walkDistance) {
		this.walkDistance = walkDistance;
	}
	public String getTrafficInfo() {
		return trafficInfo;
	}
	public void setTrafficInfo(String trafficInfo) {
		this.trafficInfo = trafficInfo;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public String getStartName() {
		return startName;
	}
	public void setStartName(String startName) {
		this.startName = startName;
	}
	public String getEndName() {
		return endName;
	}
	public void setEndName(String endName) {
		this.endName = endName;
	}
	public int getPassStopCount() {
		return passStopCount;
	}
	public void setPassStopCount(int passStopCount) {
		this.passStopCount = passStopCount;
	}
	
}
